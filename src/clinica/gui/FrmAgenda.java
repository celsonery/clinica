    private void txtPesquisaContatoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaContatoKeyReleased
        ContatoDao contato = new ContatoDao();

        if (!(evt.getKeyCode() == KeyEvent.VK_BACK_SPACE)) {
            if (!txtPesquisaContato.getText().equals("")) {
                this.lista = contato.BuscarContato(txtPesquisaContato.getText());

                if (this.lista.isEmpty()) {
                    PreencheTabela(this.lista);
                    JOptionPane.showMessageDialog(this, "Nenhum contato encontrado!");
                } else {
                    PreencheTabela(this.lista);
                }
            }
        }
    }//GEN-LAST:event_txtPesquisaContatoKeyReleased


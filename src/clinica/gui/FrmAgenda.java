    private void PreencheTabela(List<Contato> lista) {
        DefaultTableModel tabContatos = (DefaultTableModel) jTabContatos.getModel();

        tabContatos.setRowCount(0);

        if (!this.lista.isEmpty()) {
            for (int i = 0; i < lista.size(); i++) {
                tabContatos.addRow(new String[]{null, null, null, null});
                tabContatos.setValueAt(lista.get(i).getAgNome(), i, 0);
                tabContatos.setValueAt(lista.get(i).getAgTelCom1(), i, 1);
                tabContatos.setValueAt(lista.get(i).getAgTelCom2(), i, 2);
                tabContatos.setValueAt(lista.get(i).getAgTelCel(), i, 3);
            }
        }
        lblTotalContatos.setText(String.valueOf(jTabContatos.getRowCount()));
    }

    private void PreencheCampos(Contato contato) {
        txtNome.setText(contato.getAgNome());
        txtAtiv.setText(contato.getAgAtividade());
        txtEmail.setText(contato.getAgEmail());
        // Endereco
        txtCep.setText(String.valueOf(contato.getAgCep()));
        txtEnd.setText(contato.getAgEnd());
        txtComp.setText(contato.getAgComp());
        txtBairro.setText(contato.getAgBairro());
        txtCidade.setText(contato.getAgCidade());
        txtUf.setText(contato.getAgUf());
        // Telefones
        txtCom1.setText(contato.getAgTelCom1());
        txtCom2.setText(contato.getAgTelCom2());
        txtCel.setText(contato.getAgTelCel());
        txtRes.setText(contato.getAgTelRes());
        txtFax.setText(contato.getAgTelFax());
        // observacoes
        txtObs.setText(contato.getAgObs());

        this.contatoSel = contato;
    }

    private void PreencheAtividade() {
        ContatoDao contato = new ContatoDao();
        List atividade = contato.listaAtividades();

        DefaultListModel model = new DefaultListModel();
        JList list = new JList(model);

        for (int i = 0; i < atividade.size(); i++) {
            model.add(i, atividade.get(i));
        }

        ListAtividade.setModel(model);
    }


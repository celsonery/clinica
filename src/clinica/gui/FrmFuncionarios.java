    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        FuncDao funcDao = new FuncDao();

        List<Func> lista = funcDao.BuscarFunc(txtPesquisaFunc.getText());

        if (lista.size() == 1) {
            PreencheCampos(lista);
            lblMsg.setText(null);

        } else if (lista.size() > 1) {

            System.out.println("Encontrado mais de um funcionario abrindo janela de selecao");

            if (frb == null) {
                frb = new FrmResultBusca();
                frb.setLocationRelativeTo(this);
                frb.setVisible(true);
            } else {
                frb.setLocationRelativeTo(this);
                frb.setVisible(true);
            }
            frb.recebeFunc(this, lista);

        } else {
            System.out.println("Nenhum paciente encontrado!");
            lblMsg.setText("Nenhum paciente encontrado!");
            LimparCampos();
        }

        this.novoFunc = false;
    }//GEN-LAST:event_btnPesquisarActionPerformed


    private void PreencheCampos(List<Func> func) {

        txtNome.setText(func.get(0).getFncNome());
        txtCpf.setText(func.get(0).getFncCpf());
        cmbSexo.setSelectedItem(func.get(0).getFncSexo());
        txtNasc.setText(func.get(0).getFncNasc());
        txtCep.setText(String.valueOf(func.get(0).getFncCep()));
        txtEnd.setText(func.get(0).getFncEnd());
        txtBairro.setText(func.get(0).getFncBairro());
        txtCidade.setText(func.get(0).getFncCidade());
        txtUf.setText(func.get(0).getFncUf());
        txtRes.setText(func.get(0).getFncRes());
        txtCom.setText(func.get(0).getFncCom());
        txtCel.setText(func.get(0).getFncCel());
        txtFax.setText(func.get(0).getFncFax());
        txtObs.setText(func.get(0).getFncObs());
        // segunda pagina
        txtCargo.setText(func.get(0).getFncCargo());
        txtPis.setText(func.get(0).getFncPis());
        txtCtps.setText(func.get(0).getFncCtps());
        txtAdmissao.setText(func.get(0).getFncAdmissao());
        txtDemissao.setText(func.get(0).getFncDemissao());
        
        this.FuncSel = func.get(0);

    }


    private void btnSaveTabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveTabActionPerformed
        if (txtNomeTab.getText().isEmpty() || txtNomeTab.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "VocÃª precisa preencher o nome do exame/procedimento.", "Alerta", JOptionPane.ERROR_MESSAGE);
        } else {
            TabelaPrecosDao tabDao = new TabelaPrecosDao();

            if (this.tab != null) {
                this.tab.setTexNome(txtNomeTab.getText());
                this.tab.setTexCol1(txtCol1.getText());
                this.tab.setTexCol2(txtCol2.getText());

                if (tabDao.EditarTabela(this.tab) == 1) {
                    JOptionPane.showMessageDialog(this, "Tabela alterada com sucesso!");
                    fPrecos.BuscaTabelas();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao tentar editar tabela!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                TabelaPrecos tabelaPrecos = new TabelaPrecos();

                tabelaPrecos.setTexNome(txtNomeTab.getText());
                tabelaPrecos.setTexCol1(txtCol1.getText());
                tabelaPrecos.setTexCol2(txtCol2.getText());

                if (tabDao.AdicionaTabela(tabelaPrecos) == 1) {
                    JOptionPane.showMessageDialog(this, "Tabela criada com sucesso!");
                    fPrecos.BuscaTabelas();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao tentar criar nova tabela!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        
        this.dispose();
    }//GEN-LAST:event_btnSaveTabActionPerformed



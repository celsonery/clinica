    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        boolean erro = false;

        if (txtLogin.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Você deve informar um login válido para o usuário!", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
            erro = true;
        }

        if (!Arrays.equals(txtPass1.getPassword(), txtPass2.getPassword())) {
            JOptionPane.showMessageDialog(this, "As senhas não conferem!", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
            erro = true;
        }

        if (!erro) {
            UsuarioDao userDao = new UsuarioDao();

            Md5 t = new Md5();
            
            Usuario user = new Usuario();

            user.setUsrLogin(txtLogin.getText());
            user.setUsrNome(txtNome.getText());
            user.setUsrSenha(t.crypt(txtPass1.getText()));
            if(Status.isSelected()){
                user.setUsrStatus(1);
            } else {
                user.setUsrStatus(0);
            }
            user.setTblNivel_nID(cmbTipo.getSelectedIndex() + 1);
            
            if(userDao.AdicionarUsuario(user) != 1){
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar novo usuário!");
            } else {
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed


    private void PreencheTipo() {
        UsuarioDao user = new UsuarioDao();
        
        List tipos = user.BuscaTipoUsuarios();
        
        if(!tipos.isEmpty()){
            for (Object tipo : tipos) {
                cmbTipo.addItem(tipo);
            }
        }
    }
